package Exam;

public class CountTables {
    public double countX(double y, double x, double z, double a, double b){
        return y + x + z + a + b;
    }
    public double counterRound(double y, double x, double z, double a, double b){
        return y + x + z + a + b;
    }
    public double counterCross(double y, double x, double z, double a, double b, double c, double d, double s){
        return y + x + z + a + b + c + d + s;
    }
}